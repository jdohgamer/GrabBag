package com.dev.jdoh.grabbag;

class Photo {

    private String mTitle;
    private String mAuthor;
    private String mAuthorId;
    private String mLink;
    private String mTags;
    private String mImage;

    //generate constructor

    public Photo(String title, String author, String authorId, String link, String tags, String image) {
        this.mTitle = title;
        this.mAuthor = author;
        this.mAuthorId = authorId;
        this.mLink = link;
        this.mTags = tags;
        this.mImage = image;
    }

    //generate getters
    String getTitle() {
        return mTitle;
    }

    String getAuthor() {
        return mAuthor;
    }

    String getAuthorId() {
        return mAuthorId;
    }

    String getLink() {
        return mLink;
    }

    String getTags() {
        return mTags;
    }

    String getImage() {
        return mImage;
    }


    //generate toString
    @Override
    public String toString() {
        return "Photo{" +
                "mTitle='" + mTitle + '\'' +
                ", mAuthor='" + mAuthor + '\'' +
                ", mAuthorId='" + mAuthorId + '\'' +
                ", mLink='" + mLink + '\'' +
                ", mTags='" + mTags + '\'' +
                ", mImage='" + mImage + '\'' +
                '}';
    }
}
